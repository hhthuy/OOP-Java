package phan02;
/**
 * Thao tac voi toan tu pre-increment & post-increment
 * @author HongThuy
 *
 */
public class Bai04 {

	public static void main(String[] args) {
		int i = 3;
		i++;
		System.out.println(i);
		++i;
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(--i);
		System.out.println(i--);
		System.out.println(i);

	}

}
